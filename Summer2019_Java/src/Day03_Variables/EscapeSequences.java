package Day03_Variables;

public class EscapeSequences {

	/*
		
		\n (or \r): starts from a new line
		\t: horizontal tab 
		\\: prints back slash
		\': prints single quote
		\": prints double quote
	
	*/
	

	
	public static void main(String[] args) {
		
		
		System.out.println();
		
		System.out.println("Hello Cybertek\nBatch 12 Students");
		
		System.out.print("Tomorrow is off \n");
		System.out.print("True");
		System.out.println("False\n\n\n\n");
		
		System.out.println("\t\tClass starts tomorrow");
		
		System.out.println("\\Muhtar");
		System.out.println();
		System.out.println("\"Java");
		System.out.println();
		System.out.println("Book is called \" Game of Throne\" ");
		
		
		
		
		
	}
	
	
}
